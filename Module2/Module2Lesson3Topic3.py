# SORT - Sort your items
# This arranges the fruits in alphabetical order.

fruits = ["banana", "apple", "cherry"]
fruits.sort()
print(fruits)

# COUNT - If you want to know how many items are in your list. 
# This function returns the total number of items in the list.

count = len(fruits)
print(count)

# REVERSE - Reverse the order of your items. This turns the list around
numbers = [1, 2, 3, 4, 5]
print(numbers)
numbers.reverse()
print(numbers)